    private static int valorMinimo(int a, int b, int c) {
        if(a<=b && a<=c){
            return a;
        }
        if(b<=a && b<=c){
            return b;
        }
        return c;
    }



    public static int calcularDistancia(String str1, String str2) {
        char [] listaString1 = str1.toCharArray();
        char [] listaString2 = str2.toCharArray();
        int [][]distancia = new int[listaString1.length+1][listaString2.length+1];

        for(int i=0;i<=listaString1.length;i++)
        {
            distancia[i][0]=i;
        }
        for(int j=0;j<=listaString2.length;j++)
        {
            distancia[0][j]=j;
        }
        for(int i=1;i<=listaString1.length;i++)
        {
            for(int j=1;j<=listaString2.length;j++)
            {
                distancia[i][j]= valorMinimo(distancia[i-1][j]+1,
                        distancia[i][j-1]+1,
                        distancia[i-1][j-1]+
                                ((listaString1[i-1]==listaString2[j-1])?0:1));
            }
        }
        return distancia[listaString1.length][listaString2.length];

    }


